package paquete002;

public class Ciudad {

    String nombreCiudad;

    public Ciudad(String nombreCiudad) {
        this.nombreCiudad = nombreCiudad;
    }

    public void establecerNombreCiudad(String nombreCiudad) {
        this.nombreCiudad = nombreCiudad;
    }

    public String obtenerNombreCiudad() {
        return nombreCiudad;
    }

}
